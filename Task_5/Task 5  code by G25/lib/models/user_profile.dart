class UserProfile {
  final String name;
  final String id;
  final String userLocation;
  final String imageUrl;
  final String email;
  final String carModel;
  final String mobileContact;

  UserProfile({
    required this.name,
    required this.id,
    required this.userLocation,
    required this.imageUrl,
    required this.email,
    required this.carModel,
    required this.mobileContact,
  });
}